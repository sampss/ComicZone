		<h3>Rules</h3>
		<p>Generally, don't break any laws through our service.
		Certainly do not upload published comics that are not your own. Fan comics and the use of copyrighted sprites is generally okay as long as the owners of the copyrighted material do not have any problems with it.</p>

		<h3>Content Guidelines</h3>
		<p>First and foremost, we only allow the posting of content that, through the act of you posting it, is a) legal in your own country and b) legal in the United States. Generally speaking, material that would fit into the <a href="http://www.mpaa.org/ratings/what-each-rating-means">MPAA's</a> "Rated R" category and below (PG-13, G, etc.) is allowed on ComicZone.com. Material that is clearly not suitable for children requires the activation of the Mature Content warning.</p>

		<p>Material that would be considered legally <a href="http://www.lectlaw.com/def2/o002.htm">obscene</a>, including the depiction of child pornography, is explicitly banned from ComicZone.com. Comics that present material with great (or sole) priority and importance placed on evoking sexual arousal from the audience while lacking substantial quality in art, focus or presentation on other topics of interest [ie: romance, comedy, etc] are liable to be removed for bearing too close of resemblance to pornographic material rather than a comic that tackles adult subject matters.</p> 

